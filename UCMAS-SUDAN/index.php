<section id="definition" style="margin-top: -18px; ">
 <h2 > ماهو يوسي ماس
  (UCMAS)
</h2> 
<p>

<ol type="I">


هي اختصار للمفهوم الشامل للحساب الذهني    
<br>  
(UCMAS– Universal Concept of Mental Arithmetic System)
<br> <br>
<b>
كلمة اليوسي ماس
 </b>

<li>
هو برنامج تطوير عقلي مميز للأطفال مخصص لتفعيله 
طاقتهم العقلية الكامنة في المرحلة العمرية التي يبلغ فيها
العقل ذروة تطوره ونموه (أي الأطفال مابين سن الرابعة
والثانية عشرة)
.
</li>
<br>
<li> 
صمم هذا البرنامج لإستثمار السنوات الأثني عشرة الأولى  

من حياة الأطفال – حيث تكون 75 % من القدرات الدماغية
قيد النمو والتطور.
</li>
<br>
<li>
هو تقديم دورات في تنمية القدرات الذهنية والارتفاع بمستوى  
التفكير بشكل عام ليتأكد أن العملية التعليمية متعة في ذاتها
وتعد هذه الصورة الحديثة لفن الرياضيات الذهنية القديم
ويصبح طلاب يوسي ماس أكثر قدرة على التميز في جميع
المواد.
</li>
</ol>
<p id="p1">
حقق نجاح عالمي ضخم و تفوق شامل في
الحياة الاكادمية لملاين الأطفال عبر أسيا
– الخليج – الولايات المتحدة – كندا –
المملكة المتحدة – استراليا.
<br>
تأسست ال   يوسى ماس في دولة ماليزيا بواسطة بروفيسور دينو وونق  
 في العام 1993م وقد ادخل في السودان عام 2005 وقد حضر بروفيسور دينو وونق الي السودان عدة مرات بما فيها المسابقة الوطنية
<br> <br>
 
</p>
 
